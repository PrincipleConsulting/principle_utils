abstract final class SpacingSpace {
  // Small Spacing (Used for tiny gaps, padding, or margins)
  static const double sp4 = 4; // Smallest spacing, useful for tight spaces
  static const double sp5 = 5; // Slightly larger than 4px spacing
  static const double sp6 = 6; // Common for small padding in buttons or icons
  static const double sp7 = 7; // Used for minor visual separation
  static const double sp8 = 8; // Common margin for cards or elements
  static const double sp10 = 10; // Frequently used for small gaps in between items
  static const double sp12 = 12; // Standard small spacing for cards and buttons
  static const double sp14 = 14; // Medium-small space for slight separation

  // Medium Spacing (Used for regular content separation, padding, or margins)
  static const double sp16 = 16; // Ideal for regular padding or margin
  static const double sp18 = 18; // Medium space often used for content grouping
  static const double sp20 = 20; // Standard for separating sections or elements
  static const double sp22 = 22; // Slightly larger than 20px for visual breathing room
  static const double sp24 = 24; // Common for consistent separation in layouts
  static const double sp26 = 26; // Slightly larger than 24px for additional spacing

  // Large Spacing (Used for larger gaps, padding, or between major sections)
  static const double sp48 = 48; // Large spacing, typically for sections or large margins
  static const double sp50 = 50; // Slightly more than 48px, used for major separations
  static const double sp96 = 96; // Used for very large gaps between sections or cards
  static const double sp98 = 98; // Similar to 96px, used for very large margins/padding
}
